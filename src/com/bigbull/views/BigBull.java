    /**
     * Updates the table with data based on various parameters. Retrieves data
     * from the Utilities class and populates the table. The parameters include
     * transaction type, sector, sort order, sorting direction, and search text.
     */
    private void updateTable() {
        // Populate the table using data from BigBullUtilities class
        BigBullUtilities.populateTable(table,
                Objects.requireNonNull(transactionTypeJComboBox.getSelectedItem()).toString(),
                Objects.requireNonNull(sectorJComboBox.getSelectedItem()).toString(),
                Objects.requireNonNull(sortJComboBox.getSelectedItem()).toString(),
                ascendingJRadioButton.isSelected(),
                searchJTextField.getText());
    }

