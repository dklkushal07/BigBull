    /**
     * Handles the action event when the update entry button is clicked.
     * Retrieves the selected row's data from the table and opens the Form for
     * updating.
     *
     * @param evt The ActionEvent triggered by the update entry button click.
     */
    private void updateEntryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateEntryJButtonActionPerformed
        // Get the table model
        DefaultTableModel tableModel = (DefaultTableModel) table.getModel();

        // Switch based on the number of selected rows
        switch (table.getSelectedRowCount()) {
            case 0 ->
                // Display a warning if no row is selected
                JOptionPane.showMessageDialog(null,
                        "Please select a row to update!",
                        "No Rows Selected!",
                        JOptionPane.WARNING_MESSAGE);
            case 1 -> {
                // If one row is selected, retrieve data and open the update form
                int row = table.getSelectedRow();
                short symbolNo = (Short) tableModel.getValueAt(row, 0);
                String date = (String) tableModel.getValueAt(row, 1);
                String symbol = (String) tableModel.getValueAt(row, 2);
                String transactionType = (String) tableModel.getValueAt(row, 3);
                float pricePerShare = (Float) tableModel.getValueAt(row, 4);
                int quantity = (Integer) tableModel.getValueAt(row, 5);
                float totalAmount = (Float) tableModel.getValueAt(row, 6);
                float fees = (Float) tableModel.getValueAt(row, 7);
                float totalAfterFees = (Float) tableModel.getValueAt(row, 8);
                String sector = (String) tableModel.getValueAt(row, 9);
                String note = (String) tableModel.getValueAt(row, 10);
                BigBullDataModel selectedRowData = new BigBullDataModel(symbolNo,
                        date, symbol, transactionType, pricePerShare, quantity,
                        totalAmount, fees, totalAfterFees, sector, note);

                // Hide the current frame
                this.setVisible(false);

                // Open the update form with the selected row data
                BigBullForm updateForm = new BigBullForm(selectedRowData);
                updateForm.setVisible(true);
            }
            default ->
                // Display a warning if multiple rows are selected
                JOptionPane.showMessageDialog(null,
                        "Please select only one row to update!",
                        "Multiple Rows Selected!",
                        JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_updateEntryJButtonActionPerformed

    /**
     * Handles the action event when the delete entry button is clicked. Deletes
     * the selected row from the table after confirming the deletion with the
     * user.
     *
     * @param evt The ActionEvent triggered by the delete entry button click.
     */
    private void deleteEntryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteEntryJButtonActionPerformed
        // Switch based on the number of selected rows
        switch (table.getSelectedRowCount()) {
            case 0 ->
                // Display a warning if no row is selected
                JOptionPane.showMessageDialog(null, "Please select a row to delete!", "No Rows Selected!", JOptionPane.WARNING_MESSAGE);
            case 1 -> {
                // If one row is selected, confirm deletion with the user
                int userConfirmation = JOptionPane.showConfirmDialog(null,
                        "Do you sure want to delete this row? This action is permanent and cannot be reverted!",
                        "Delete Row!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

                if (userConfirmation == JOptionPane.YES_OPTION) {
                    // If the user confirms, delete the selected row
                    DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
                    int rowIndex = table.getSelectedRow();
                    short symbolNoOfRowToDelete = (Short) tableModel.getValueAt(rowIndex, 0);
                    BigBullUtilities.deleteRowFromTable(symbolNoOfRowToDelete);

                    // Update the table after deletion
                    updateTable();
                }
            }
            default ->
                // Display a warning if multiple rows are selected
                JOptionPane.showMessageDialog(null, "Please select only one row to delete!", "Multiple Rows Selected!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteEntryJButtonActionPerformed


